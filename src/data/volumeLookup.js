export const getLogVolume = (length, diameter) => {
  const lengthKey = parseFloat(length);
  const diameterKey = parseFloat(diameter);
  
  if (VOLUME_LOOKUP[lengthKey] && VOLUME_LOOKUP[lengthKey][diameterKey]) {
    return VOLUME_LOOKUP[lengthKey][diameterKey];
  }
  return 0;
};

